/**
 * _isupper - a function that checks for an uppercase character.
 * @c : input parameter
 * Return: 1 if c is uppercase, else return 0.
 */

int _isupper(int c)
{
	int i;
	char A, Z, j;

	for (i = 0; i <= 25; i++)
	{
		for (j = A; j <= Z; j++)
		{
			if ( c = j)
			return (1);	
		}	
	}

	return (0);
}
